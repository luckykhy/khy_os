/* framebuffer.c — Linear framebuffer driver for KHY OS
 *
 * Parses Multiboot2 framebuffer tag to obtain the linear framebuffer
 * address, then provides pixel-level drawing and text rendering.
 *
 * The embedded 8x16 font covers ASCII 0x20-0x7E (95 printable characters).
 * Font data is a subset of the classic VGA/PC BIOS font.
 * @pattern Adapter
 */

